<p>Visi dan Misi Polri <br
>
<br>Visi
<br>
Terwujudnya pelayanan keamanan dan ketertiban masyarakat yang prima, tegaknya hukum dan keamanan dalam negeri yang mantap serta terjalinnya sinergi polisional yang proaktif.
<br>
Misi
<br>
1.Melaksanakan deteksi dini dan peringatan dini melalui kegiatan/operasi penyelidikan, pengamanan dan penggalangan;<br>
2.Memberikan perlindungan, pengayoman dan pelayanan secara mudah, responsif dan tidak diskriminatif;<br>
3.Menjaga keamanan, ketertiban dan kelancaran lalu lintas untuk menjamin keselamatan dan kelancaran arus orang dan barang;<br>
4.Menjamin keberhasilan penanggulangan gangguan keamanan dalam negeri;<br>
5.Mengembangkan perpolisian masyarakat yang berbasis pada masyarakat patuh hukum;<br>
Menegakkan hukum secara profesional, objektif, proporsional, transparan dan akuntabel untuk menjamin kepastian hukum dan rasa keadilan;<br>
6.Mengelola secara profesional, transparan, akuntabel dan modern seluruh sumber daya Polri guna mendukung operasional tugas Polri;<br>
7.Membangun sistem sinergi polisional interdepartemen dan lembaga internasional maupun komponen masyarakat dalam rangka membangun kemitraan dan jejaring kerja (partnership building/networking)
